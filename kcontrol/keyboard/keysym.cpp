KeySym::KeySym()
{
    nill = 0;
}

QString KeySym::getkeyuni(QString opton){
    QRegExp krx("^U[0-9]");

    QString opton2(opton);
    if(opton2.contains(krx)){
        opton2.replace("U", "0x");
    }

    if( opton2.startsWith("0x") ) {
        bool ok;
        int code = opton2.mid(2).toInt(&ok, 16);
        
        if( ok ) {
            QString str = QString(QChar(code));
            if( code == 0x0301 ) {
                str = " " + str;
            }
            return str;
        }
    }

    for(int i = 0; ! keystr[i].isNull(); i++){
        if(keystr[i] == opton2)
            return QString(keyuni[i]);
    }
    
    nill++;
    
    return " ";
}

